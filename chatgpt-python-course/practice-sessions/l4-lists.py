# Create a list of your favorite books
books = ["Born to Run", "Steve Jobs"]

# Add a new book to the list
books.append("Personalization at Work")

# Print the first and last books from the list
print(books[0])  # Prints the first book "Born to Run"
print(books[2])  # Prints the last book "Personalization at Work"

# Use a for loop to print all the books in the list
for book in books:
    print(book)